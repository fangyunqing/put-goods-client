class ContentType:
    """
        MIME类型
    """

    # HTML文档标记
    HTML = "text/html"
    # JPEG图片标记
    JPEG = "image/jpeg"
    # GIF图片标记
    GIF = "image/gif"
    # JS文档标记
    JS = "application/javascript"
    # XML文件标记
    XML = "application/xml"
    # FORM表单提交
    FORM = "application/x-www-form-urlencoded"
    # 多媒体
    FORM_DATA = "multipart/form-data"
    # JSON
    JSON = "application/json"
